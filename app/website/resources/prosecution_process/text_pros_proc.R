  <h4 class='bold'>
      What is a Trial?
  </h4>
  <p>
      A criminal trial is a formal examination of evidence in a court of law
      before a judge or a jury to determine whether the charged individual is
      guilty of the charges brought against them beyond a reasonable doubt. Cases
      that do not result in a plea or dismissal proceed to trial. There are two
      types of trials: A jury trial and a bench trial. In a bench trial, the
      judge hears all evidence and serves as the sole decision-maker. A jury
